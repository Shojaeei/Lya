    def __init__(self, output_dir: str | Path | None = None) -> None:
        """Initialize generator.

        Args:
            output_dir: Directory to save generated capabilities
        """
        self.output_dir = Path(output_dir) if output_dir else Path("./generated_capabilities")
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def identify_missing_capabilities(
        self,
        usage_patterns: list[dict[str, Any]],
        existing_capabilities: list[str]
    ) -> list[CapabilitySpec]:
        """
        Identify missing capabilities based on usage patterns.

        Args:
            usage_patterns: List of usage attempts and failures
            existing_capabilities: List of existing capability names

        Returns:
            List of capability specs that should be created
        """
        missing: list[CapabilitySpec] = []

        # Analyze patterns for common unmet needs
        for pattern in usage_patterns:
            if pattern.get("resolved"):
                continue

            description = pattern.get("description", "").lower()

            # Pattern matching for common needs
            if "csv" in description and "csv" not in existing_capabilities:
                missing.append(self._create_csv_spec())

            elif "json" in description and "json" not in existing_capabilities:
                missing.append(self._create_json_spec())

            elif "image" in description and "image" not in existing_capabilities:
                missing.append(self._create_image_spec())

            elif "pdf" in description and "pdf" not in existing_capabilities:
                missing.append(self._create_pdf_spec())

            elif "compression" in description or "zip" in description:
                if "compression" not in existing_capabilities:
                    missing.append(self._create_compression_spec())

            elif "database" in description or "sqlite" in description:
                if "database" not in existing_capabilities:
                    missing.append(self._create_database_spec())

        # Remove duplicates
        seen = set()
        unique = []
        for spec in missing:
            if spec.name not in seen:
                seen.add(spec.name)
                unique.append(spec)

        return unique


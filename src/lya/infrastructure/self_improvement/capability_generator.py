class SelfImprovementLoop:
    """
    Main self-improvement loop for Lya.

    Continuously analyzes usage patterns and generates new capabilities.
    """

    def __init__(
        self,
        generator: CapabilityGenerator | None = None,
        check_interval_minutes: int = 5,
    ) -> None:
        """Initialize improvement loop.

        Args:
            generator: Capability generator
            check_interval_minutes: How often to check for improvements
        """
        self.generator = generator or CapabilityGenerator()
        self.check_interval = check_interval_minutes * 60  # Convert to seconds
        self.running = False

    def analyze_and_improve(
        self,
        usage_patterns: list[dict[str, Any]],
        existing_capabilities: list[str]
    ) -> dict[str, Any]:
        """
        Analyze patterns and generate missing capabilities.

        Args:
            usage_patterns: List of usage attempts
            existing_capabilities: Existing capability names

        Returns:
            Results of improvement attempt
        """
        results = {
            "analyzed_at": datetime.now(timezone.utc).isoformat(),
            "missing_found": 0,
            "generated": [],
            "failed": [],
        }

        # Identify missing capabilities
        missing = self.generator.identify_missing_capabilities(
            usage_patterns,
            existing_capabilities,
        )

        results["missing_found"] = len(missing)

        # Generate each capability
        for spec in missing:
            try:
                capability, validation = self.generator.generate_and_save(spec)

                if validation.success:
                    results["generated"].append({
                        "name": spec.name,
                        "path": str(self.generator.output_dir / f"{spec.name}.py"),
                        "validation": validation.__dict__,
                    })
                else:
                    results["failed"].append({
                        "name": spec.name,
                        "reason": validation.errors,
                    })

            except Exception as e:
                results["failed"].append({
                    "name": spec.name,
                    "reason": [str(e)],
                })

        return results



"""Capability Generator for Lya Self-Improvement.

Generates new capabilities based on need analysis and validates them.
Pure Python 3.14+ compatible.
"""

from __future__ import annotations

import ast
import hashlib
import json
import re
import subprocess
import tempfile
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any
